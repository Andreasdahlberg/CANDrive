__attribute__((weak)) void NVS_Init(uint32_t start_page_address, size_t number_of_pages)
{
    function_called();
}
__attribute__((weak)) bool NVS_Retrieve(const char *key_p, uint32_t *value_p)
{
    assert_non_null(key_p);
    assert_non_null(value_p);

    *value_p = mock_type(uint32_t);
    return mock_type(bool);
}
__attribute__((weak)) bool NVS_Store(const char *key_p, uint32_t value)
{
    assert_non_null(key_p);

    return mock_type(bool);
}
__attribute__((weak)) bool NVS_Clear(void)
{
    return mock_type(bool);
}

